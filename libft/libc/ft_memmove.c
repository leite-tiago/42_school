#include "libft.h"

void	*ft_memmove(void *dst, const void *src, int len)
{
	int i;

	if (!dst && !src)
		return (0);
	i = 0;
	if ((int)dst - (int)src < len)
	{
		i = len - 1;
		while (i < len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i--;
		}
	}
	else
	{
		while (i < len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	return (dst);
}
